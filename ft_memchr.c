#include <stdlib.h>
#include "libft.h"

void	*ft_memchr(void *s, int c, size_t n)
{
	size_t		i;

	i = 0;
	while (i < n)
	{
		if (((unsigned char *)s)[i] == (unsigned char)c)
			return ((void *)((char *)s + i));
		i++;
	}
	return (NULL);
}
