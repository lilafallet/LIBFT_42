#include "libft.h"

void	ft_lstdel(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!lst || !*lst)
		return ;
	while (*lst)
	{
		tmp = (*lst)->next;
		if (del != NULL)
			del((*lst)->content);
		free(*lst);
		*lst = NULL;
		*lst = tmp;
	}
}
